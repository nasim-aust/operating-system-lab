MOV AL,80H
OUT 1EH,AL
MOV AL,0FFH
OUT 1AH, AL

RESTART:
MOV BL,0FEH
MOV DL,01H  

MOV CX,08H 
LOOP1:
MOV AL,BL
OUT 18H,AL
MOV AL,DL
OUT 1CH,AL
CALL DELAY
SHL BL,01H
SHL DL,01H
OR DL,01H

LOOP LOOP1
  
  
MOV BL,80H
MOV DL,07FH

MOV CX,08H
LOOP2:
MOV AL,BL
OUT 18H,AL
MOV AL,DL
OUT 1CH,AL
CALL DELAY
SHR DL,01H
SHR BL,01H
OR BL,80H

LOOP LOOP2 

JMP RESTART

DELAY:
PUSH CX
MOV CX,0FF00H
LOOP3:
NOP
LOOP LOOP3

POP CX
RET